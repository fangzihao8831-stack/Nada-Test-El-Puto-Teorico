#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(__dirname, "..");
const projectRoot = resolve(appRoot, "..");

const structure = JSON.parse(readFileSync(join(projectRoot, "content", "content-structure.json"), "utf-8"));
const subtemaToTema = {};
const temaNames = {};
for (const tema of structure.temas) {
  temaNames[tema.id] = tema.nombre;
  for (const sub of tema.subtemas) subtemaToTema[sub.id] = tema.id;
}

const catalogoPath = join(projectRoot, "content", "imagenes", "senales", "catalogo.json");
const senalMap = {};
if (existsSync(catalogoPath)) {
  for (const s of JSON.parse(readFileSync(catalogoPath, "utf-8"))) senalMap[s.codigo] = s.archivo;
}

const batchName = process.argv[2] || "batch_03";
const batchPath = join(projectRoot, "content", "preguntas", batchName);
const file = readdirSync(batchPath).find(f => f.endsWith(".json") && !f.includes("_validated"));
const { preguntas } = JSON.parse(readFileSync(join(batchPath, file), "utf-8"));

const bank = preguntas.map(q => {
  const temaId = subtemaToTema[q.subtema_id] || "tema_00";
  return {
    id: q.id, number: 0,
    enunciado: q.enunciado,
    opciones: q.opciones.map((texto, i) => ({ key: String.fromCharCode(65 + i), texto })),
    correcta: String.fromCharCode(65 + (q.correcta || 0)),
    explicacion: q.explicacion,
    hasImage: false,
    imageSrc: q.codigo_señal && senalMap[q.codigo_señal] ? "/images/senales/" + senalMap[q.codigo_señal] : null,
    temaId, tema: temaNames[temaId] || "Desconocido",
    pista: q.pista || "",
    tipoPregunta: q.tipo_pregunta,
    nivel: q.nivel || null,
  };
});

writeFileSync(join(appRoot, "src", "lib", "question-bank.ts"), `import type { TestQuestion } from "./mock-test-data";

/**
 * Validated DGT questions from the content pipeline.
 * Auto-generated by scripts/generate-question-bank.mjs — do not edit manually.
 */

export interface BankQuestion extends TestQuestion {
  temaId: string;
}

export const questionBank: BankQuestion[] = ${JSON.stringify(bank, null, 2)};
`);

console.log(`Generated ${bank.length} questions (${batchName})`);
